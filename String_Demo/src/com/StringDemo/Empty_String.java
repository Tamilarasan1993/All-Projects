package com.StringDemo;

public class Empty_String {
	public static void main(String[] args) {
		String s = "Tom";
		String s1 = " ";
		String s2 = "";
		String s3 =null;
		
		System.out.println("Empty or Not: "+s.isEmpty());
		System.out.println("Empty or Not :"+s1.isEmpty());
		System.out.println("Empty or Not :"+s2.isEmpty());
		System.out.println("Empty or Not :"+s3.isEmpty());
	}
}