package com.looop;

public class Loop_Pyramid {
	public static void main(String[] args) {
		
		int i,j;
		
	
	for (i=1; i<=9; i++) {
		for (j=1; j<=i; j++) {
			System.out.print(j +" ");
		}
		System.out.println();

	}
	for (i=8; i>=1; i--) {
		for (j=1; j<=i; j++) {
			System.out.print(j +" ");
		}
		System.out.println();
	
}
	

}
}


