package com.SelectionStatement;

public class Demo_Switch {
	
	public static void main(String[] args) {
		
		int i = 1;
		
		switch (i) {
		case 1:
			System.out.println("Value is one");
			
			//break;
		case 2:
			System.out.println("TAMIL");
			break;
			
		default:
			System.out.println("invalid");
			break;
		}
	}

}
